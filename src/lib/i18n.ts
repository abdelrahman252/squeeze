export function useTranslation() {
  const lang = useLang() as Language;
  const isRtl = lang === "ar";
  
  const t = (key: keyof typeof translations["en"]) => {
    return translations[lang][key] || translations["en"][key] || key;
  };
  
  return { t, lang, isRtl };
}
